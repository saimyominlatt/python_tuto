# len() //function
# .upper() .lower() .capitalize() .find() .replace //method
# '...' in course //in operator


course = "Msai xitu m is Learning python now."
print(len(course))

print(course)
print(course.upper())
print(course.lower())
print(course.capitalize())

print(course.find('M'))
print(course.find('m'))  # case sensitive
print(course.find('is'))
print(course.replace('is', 'was'))

print("Msai" in course)  # in operator
print("msai" in course)

